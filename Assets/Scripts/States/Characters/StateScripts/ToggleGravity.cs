using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[CreateAssetMenu(fileName = "New State", menuName = "KarakuriFestival/AbilityData/ToggleGravity")]
public class ToggleGravity : StateData
{
    public bool On;
    public bool OnStart;
    public bool OnEnd;

    public override void OnEnter(CharacterState characterState, Animator animator, AnimatorStateInfo stateInfo)
    {
        if (OnStart)
        {
            CharacterControl control = characterState.GetCharacterControl(animator);
            ToggleGrav(control);
        }
    }

    public override void UpdateAbility(CharacterState characterState, Animator animator, AnimatorStateInfo stateInfo)
    {

    }

    public override void OnExit(CharacterState characterState, Animator animator, AnimatorStateInfo stateInfo)
    {
        if (OnEnd)
        {
            CharacterControl control = characterState.GetCharacterControl(animator);
            ToggleGrav(control);
        }
    }

    private void ToggleGrav(CharacterControl control)
    {
        control.RIGID_BODY.velocity = Vector3.zero;
        control.RIGID_BODY.useGravity = On;
    }
}